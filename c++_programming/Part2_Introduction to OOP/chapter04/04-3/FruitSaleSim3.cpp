// 멤버 이니셜라이저(Member Initializer)를 이용한 변수 및 const 초기화
// Initialize variables with member Initializer and constants with const 
#include<iostream>
using namespace std;
class FruitSeller{
private:
    const int APPLE_PRICE;
    int numOfApples;
    int myMoney;
public:
    FruitSeller(int price,int num,int money):APPLE_PRICE(price),numOfApples(num),myMoney(money){}
    int SaleApples(int money){
        if(money<0){
            cout<<"잘못된 정보가 전달되어 판매를 취소합니다."<<endl;
            return 0;
        }
        int num=money/APPLE_PRICE;
        numOfApples-=num;
        myMoney+=money;
        return num;
    }
    void ShowSalesResult(){
        cout<<"과일 판매자의 현황"<<endl;
        cout<<"남은 사과: "<<numOfApples<<endl;
        cout<<"판매 수익: "<<myMoney<<endl<<endl;
    }
};
class FruitBuyer{
private:
    int myMoney;
    int numOfApples;
public:
    FruitBuyer(int money):myMoney(money),numOfApples(0){}
    void BuyApples(FruitSeller &seller,int money){
        if(money<0){
            cout<<"잘못된 정보가 전달되어 구매를 취소합니다."<<endl;
        }
        numOfApples+=seller.SaleApples(money);
        myMoney-=money;
    }
    void ShowBuyResult(){
        cout<<"과일 구매자의 현황"<<endl;
        cout<<"현재 잔액: "<<myMoney<<endl;
        cout<<"사과 개수: "<<numOfApples<<endl;
    }
};
int main(void){
    FruitSeller seller(1000,20,0);
    FruitBuyer buyer(5000);
    buyer.BuyApples(seller,2000);
    seller.ShowSalesResult();
    buyer.ShowBuyResult();
}